package koem.com.batch.service;

public class HaguVO {

	/** 정점코드 */
	private String STA_CDE;
	
	/** 관측일 */
	private String OBS_DAT;
	
	/** 정점명 */
	private String STA_NAM;
	
	/** 수소이온농도 */
	private String PH;
	
	/** 화학산소요구량(㎎/L) */
	private String COD;
	
	/** 탁도(NTU) */
	private String NTU;
	
	/** 수온(℃) */
	private String WTEMP;
	
	/** 용존산소(㎎/L) */
	private String DOX;
	
	/** 염분 */
	private String SALINITY;

	public String getSTA_CDE() {
		return STA_CDE;
	}

	public void setSTA_CDE(String sTA_CDE) {
		STA_CDE = sTA_CDE;
	}

	public String getOBS_DAT() {
		return OBS_DAT;
	}

	public void setOBS_DAT(String oBS_DAT) {
		OBS_DAT = oBS_DAT;
	}

	public String getSTA_NAM() {
		return STA_NAM;
	}

	public void setSTA_NAM(String sTA_NAM) {
		STA_NAM = sTA_NAM;
	}

	public String getPH() {
		return PH;
	}

	public void setPH(String pH) {
		PH = pH;
	}

	public String getCOD() {
		return COD;
	}

	public void setCOD(String cOD) {
		COD = cOD;
	}

	public String getNTU() {
		return NTU;
	}

	public void setNTU(String nTU) {
		NTU = nTU;
	}

	public String getWTEMP() {
		return WTEMP;
	}

	public void setWTEMP(String wTEMP) {
		WTEMP = wTEMP;
	}

	public String getDOX() {
		return DOX;
	}

	public void setDOX(String dOX) {
		DOX = dOX;
	}

	public String getSALINITY() {
		return SALINITY;
	}

	public void setSALINITY(String sALINITY) {
		SALINITY = sALINITY;
	}

	
	
	
	
	
	
}
